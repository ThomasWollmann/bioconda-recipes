export LD_LIBRARY_PATH=${PREFIX}/lib

cd contrib

cmake . -DBUILD_TYPE=LIBSVM
#cmake . -DBUILD_TYPE=XERCESC
#cmake . -DBUILD_TYPE=GSL
cmake . -DBUILD_TYPE=COINOR
#cmake . -DBUILD_TYPE=BZIP2
