#cmake . -DBUILD_TYPE=EIGEN
cmake . -DBUILD_TYPE=WILDMAGIC
cmake . -DBUILD_TYPE=SEQAN
#cmake . -DBUILD_TYPE=BOOST -DNUMBER_OF_JOBS=4
cd ..

mkdir build
cd build
ORIGIN='$ORIGIN'
